def get1(n):
    return 'M' * n

def get2(n):
    if n < 4:
        return 'C' * n 
    elif n == 4:
        return 'CD'
    elif 4 < n < 9:
        return 'D' + ('C' * (n - 5))
    elif n == 9:
        return 'CM'

def get3(n):
    if n < 4:
        return 'X' * n 
    elif n == 4:
        return 'XL'
    elif 4 < n < 9:
        return 'L' + ('X' * (n - 5))
    elif n == 9:
        return 'XC'

def get4(n):
    if n < 4:
        return 'I' * n 
    elif n == 4:
        return 'IV'
    elif 4 < n < 9:
        return 'V' + ('I' * (n - 5))
    elif n == 9:
        return 'IX'

def arabic_to_roman(inp):
	num = int(inp)
	ln = []
	lst = []

	ln = [num // 1000, num % 1000 // 100, num % 100 // 10, num % 10]
	lst = [get1(ln[0]), get2(ln[1]), get3(ln[2]), get4(ln[3])]
	return ''.join(lst)

inp = input()
print(arabic_to_roman(inp))
